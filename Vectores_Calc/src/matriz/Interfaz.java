    private void btnAgregarMActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarMActionPerformed
        if(cboxFilaM.getSelectedItem().equals("0")||cboxColumM.getSelectedItem().equals("0")){
            JOptionPane.showMessageDialog(rootPane, "SELECCIONA VALORES PARA EL TAMAÑO DE LA MATRIZ");
        }else if(cboxFilaM.getSelectedItem()!=cboxColumM.getSelectedItem()){
                JOptionPane.showMessageDialog(rootPane, "Las filas y columnas DEBEN TENE EL MISMO TAMAÑO");
            }else{
            int setX = 20, setY = 20;
            filas=Integer.parseInt((String) cboxFilaM.getSelectedItem());
            colum = Integer.parseInt((String) cboxColumM.getSelectedItem());
            m1=new JTextField[filas][colum];

            for (int i = 0; i < filas; i++) {
                for (int j = 0; j < colum; j++) {
                    m1[i][j] = new JTextField(" ");
                    m1[i][j].setBounds(setX, setY, 30, 30);
                    m1[i][j].setHorizontalAlignment(JTextField.RIGHT);
                    setX += 35;
                }
                setX = 20;
                setY += 35;
            }

            for (int z = 0; z < filas; z++) {
                for (int y = 0; y < colum; y++) {
                    jPaneM.add(m1[z][y]);
                    m1[z][y].setVisible(false);
                }
            }
            for (int l = 0; l < filas; l++) {
                for (int j = 0; j < colum; j++) {
                    m1[l][j].setVisible(true);
                }
            
            }
        }
    }//GEN-LAST:event_btnAgregarMActionPerformed


    public double[][] tomarValores(JTextField mOperaciones[][]) {
 
        double mRetornada[][] = new double[mOperaciones.length][mOperaciones[0].length];
 
        for (int i = 0; i < mOperaciones.length; i++) {
            for (int j = 0; j < mOperaciones[0].length; j++) {
                mRetornada[i][j] = Double.parseDouble(mOperaciones[i][j].getText());
            }
        }
 
        return mRetornada;
    }
    
    public double[] vector(double [][] a){
        double vec [] = new double[a.length];
        for (int i=0; i<a.length;i++){
            vec[i]=a[i][0];
        }
        return vec;
    }
    

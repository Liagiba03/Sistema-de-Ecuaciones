    private void btnAgregarRActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarRActionPerformed
        if(cboxFilaR.getSelectedItem().equals("0")){
            JOptionPane.showMessageDialog(rootPane, "SELECCIONA VALORES PARA EL TAMAÑO DE LA MATRIZ");
        }else{
            int setX = 20, setY = 20;
            filas=Integer.parseInt((String) cboxFilaR.getSelectedItem());
            colum = 0;
            m2=new JTextField[filas][1];

            for (int i = 0; i < filas; i++) {
                
                    m2[i][colum] = new JTextField(" ");
                    m2[i][colum].setBounds(setX, setY, 30, 30);
                    
                setY += 35;
            }

            for (int z = 0; z < filas; z++) {
                
                    jPaneR.add(m2[z][colum]);
                    m2[z][colum].setVisible(false);
                
            }
            for (int l = 0; l < filas; l++) {
                
                    m2[l][colum].setVisible(true);
                
            }
        }
    }//GEN-LAST:event_btnAgregarRActionPerformed


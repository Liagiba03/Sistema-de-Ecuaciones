    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        RealMatrix ecuaciones = new Array2DRowRealMatrix(tomarValores(m1));
        DecompositionSolver solver = new LUDecomposition(ecuaciones).getSolver();
        RealVector constantes = new ArrayRealVector(vector(tomarValores(m2)));
        RealVector solucion = solver.solve(constantes);
        
        txtResult.setText(solucion.toString());
        
    }//GEN-LAST:event_btnCalcularActionPerformed

